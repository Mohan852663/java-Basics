//switch statement in java
public class switchstatement{
	public static void main(String[] args){
		//declare switch statements
		int number=50;
		switch(number){
			case 10: System.out.println("hai");
			break;
			case 20: System.out.println("hello");
			break;
			case 30: System.out.println("buddy");
			break;
		//default
			default : System.out.println("invalid");
		}
	}
}